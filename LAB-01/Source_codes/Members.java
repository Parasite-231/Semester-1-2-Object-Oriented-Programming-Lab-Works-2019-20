class Members{//In this class concept of Encapsulation is used,where only using setter and getter method only methods can be accessed in class.
	
	public	String work,member,student ;


    public void setWork(String work){
		this.work = work ;
	} 
	public void setMember(String member){
		this.member = member ;
	} 
	public void setStudent(String student){
		this.student = student ;
	} 
	public String getWork(){
		return work ;
	}
	public String getMember(){
		return member ;
	}
	public String getStudent(){
		return student ;
	}
}